package com.kyawn.entity;

public class Dorm {

	private String DormName;
	private String Bname;
	private Long DormStorage;
	private Long DormLeft;
	private String DormAdmin;
	private String DormPhone;

	public String getDormName() {
		return DormName;
	}

	public void setDormName(String dormName) {
		DormName = dormName;
	}

	public String getBname() {
		return Bname;
	}

	public void setBname(String bname) {
		Bname = bname;
	}

	public Long getDormStorage() {
		return DormStorage;
	}

	public void setDormStorage(Long dormStorage) {
		DormStorage = dormStorage;
	}

	public Long getDormLeft() {
		return DormLeft;
	}

	public void setDormLeft(Long dormLeft) {
		DormLeft = dormLeft;
	}

	public String getDormAdmin() {
		return DormAdmin;
	}

	public void setDormAdmin(String dormAdmin) {
		DormAdmin = dormAdmin;
	}

	public String getDormPhone() {
		return DormPhone;
	}

	public void setDormPhone(String dormPhone) {
		DormPhone = dormPhone;
	}

}
