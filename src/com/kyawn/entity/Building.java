package com.kyawn.entity;

public class Building {

	private String buildingname;
	private String buildingadmin;
	private String buildingphone;

	public String getBuildingname() {
		return buildingname;
	}

	public void setBuildingname(String buildingname) {
		this.buildingname = buildingname;
	}

	public String getBuildingadmin() {
		return buildingadmin;
	}

	public void setBuildingadmin(String buildingadmin) {
		this.buildingadmin = buildingadmin;
	}

	public String getBuildingphone() {
		return buildingphone;
	}

	public void setBuildingphone(String buildingphone) {
		this.buildingphone = buildingphone;
	}

}
